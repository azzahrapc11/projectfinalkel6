    private void tblBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBukuMouseClicked
        // TODO add your handling code here:
        int i = tblBuku.getSelectedRow();
        if( i == -1)
        {
            return;
        }
        buku = tableModelBuku.select(i);
        txtKodeBuku.setText((String) tableModelBuku.getValueAt(i, 0));
        txtJudulBuku.setText((String) tableModelBuku.getValueAt(i, 1));
        txtPengarang.setText((String) tableModelBuku.getValueAt(i, 2));
        txtPenerbit.setText((String) tableModelBuku.getValueAt(i, 3));
        txtTahunTerbit.setText((String) tableModelBuku.getValueAt(i, 4));
        txtStok.setText(tableModelBuku.getValueAt(i, 5).toString());
        cbStatus.setSelectedItem(tableModelBuku.getValueAt(i, 6));
        
        btnUbah.setEnabled(false);
        btnUbah.setEnabled(true);
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_tblBukuMouseClicked

