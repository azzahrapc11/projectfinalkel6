    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        int i = tblBuku.getSelectedRow();
        if(i == -1)
        {
            JOptionPane.showMessageDialog(null, "Pilih baris yang akan diubah!");
            return;
        }
        
        buku.setBkKd(txtKodeBuku.getText());
        buku.setBkJudul(txtJudulBuku.getText());
        buku.setBkPengarang(txtPengarang.getText());
        buku.setBkPenerbit(txtPenerbit.getText());
        buku.setBkTahunTerbit(txtTahunTerbit.getText());
        buku.setBkStok(Integer.valueOf(txtStok.getText()));
        buku.setBkStatus((String)cbStatus.getSelectedItem());
        
        try
        {
            bukujpa.editBuku(buku);
            JOptionPane.showMessageDialog(this, "Edit Data Buku Berhasil!");
        }
        catch(Exception e)
        {
            System.out.println("Error Saat Memanggil Method Edit di Controller" +e);
        }
        
        refresh();
        clear();
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int row = tblBuku.getSelectedRow();
        if(row != -1)
        {
            if(JOptionPane.showConfirmDialog(this, "Apakah anda yakin untuk menghapus data?", "Konfirmasi",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.CANCEL_OPTION)
            {
                return;
            }
        }
        
        try
        {
            bukujpa.hapusBuku(list.get(row));
            JOptionPane.showMessageDialog(this, "Hapus Data Buku Berhasil!");
        }
        catch(Exception e)
        {
            System.out.println("Error Saat Memanggil Method Hapus di Controller" +e);
        }
        
        refresh();
        clear();
    }//GEN-LAST:event_btnHapusActionPerformed

