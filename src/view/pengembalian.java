    public void load(){
        String idPinjam = txtKdTransaksi.getText();
        tblDetilTransaksi.setModel(tableModelDetilBuku);
        if(idPinjam.equals(null) || idPinjam.equals(""))
        {
            tableModelDetilBuku.deleteall();
            JOptionPane.showMessageDialog(this, "Kode Transaksi Tidak Ditemukan!");
        }
        else
        {
            tableModelDetilBuku.deleteall();
            listDetil = detiljpa.tampilDetilPenjualan(idPinjam);
            for (TblDetilTransaksi transaksi : listDetil) {
                tableModelDetilBuku.insert(transaksi);
            }
        }
    }
    
    private void updateStok(String idBuku , int jumlah){
        TblBuku buku = new TblBuku();
        listBuku = bukujpa.tampilID(idBuku);
        int stok_temp = listBuku.get(0).getBkStok() + jumlah;
        
        buku.setBkKd(idBuku);
        buku.setBkJudul(listBuku.get(0).getBkJudul());
        buku.setBkPengarang(listBuku.get(0).getBkPengarang());
        buku.setBkPenerbit(listBuku.get(0).getBkPenerbit());
        buku.setBkTahunTerbit(listBuku.get(0).getBkTahunTerbit());
        buku.setBkStok(stok_temp);
        
        if(stok_temp != 0)
        {
            buku.setBkStatus("Tersedia");
        }
        else
        {
            buku.setBkStatus(listBuku.get(0).getBkStatus());
        }
        
        try
        {
            bukujpa.editBuku(buku);
        }
        catch(Exception ex)
        {
            System.out.println("error saat mengembalikan stok buku " +ex);
        }
    }
    
