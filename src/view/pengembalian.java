    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        TblTransaksi transaksi = new TblTransaksi();
        
        transaksi.setTransKd(txtKdTransaksi.getText());
        transaksi.setTransTglTransaksi(txtTanggalTransaksi.getDate());
        transaksi.setMbKd(txtKdMember.getText());
        transaksi.setTransTglPinjam(txtTanggalPinjam.getDate());
        transaksi.setTransTglKembali(txtTanggalKembali.getDate());
        transaksi.setStatusPengembalian("Sudah");
        
        try
        {
            //menyimpan data di penjualan dan member
            peminjamanjpa.editPeminjaman(transaksi);
        } 
        catch(HeadlessException e) 
        {
            System.out.println("Error saat panggil method simpan di controller "+e);
            JOptionPane.showMessageDialog(null, "Data tidak berhasil disimpan!");
        }
        
        int j = tblDetilTransaksi.getModel().getRowCount();
        
        for (int k = 0; k < j; k++) 
        {
            //mengambil id buku berdasarkan kolom pertama baris ke k
            String idBuku = "";
            int JumlahPinjam;
            
            idBuku = tableModelDetilBuku.getValueAt(k, 1).toString();
            JumlahPinjam = Integer.valueOf(tableModelDetilBuku.getValueAt(k, 3).toString()); //mengambil data jumlah item dari kolom ke 4 baris ke k
            
            try 
            {
                updateStok(idBuku, JumlahPinjam); // menambah data transaksi
                
            } catch (Exception e) {
                System.out.println("Terjadi error saat panggil method update stok buku: " + e);
            }
        }
        JOptionPane.showMessageDialog(this, "Berhasil mengembalikan buku!");
        clear();
    }//GEN-LAST:event_btnSimpanActionPerformed

